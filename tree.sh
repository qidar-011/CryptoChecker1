#!/bin/bash

# دالة لعرض شجرة الملفات
function tree() {
    # تكرار عبر جميع الملفات والمجلدات في المسار الحالي
    for file in *; do
        # إذا كان العنصر مجلد
        if [ -d "$file" ]; then
            echo "|-- $file"
            # التنقل إلى المجلد وتشغيل الدالة عليه لعرض الملفات داخله
            (cd "$file" && tree)
        else
            # إذا كان العنصر ملف
            echo "|-- $file"
        fi
    done
}

# استدعاء الدالة
tree
